# proporciones.py

# Pedir al usuario un número
x = float(input("Ingresa un número: "))

# Calcular proporciones
mitad = x * 0.5
cuarta_parte = x * 0.25
decima_parte = x * 0.10
cinco_por_ciento = x * 0.05
dos_por_ciento = x * 0.02

# Mostrar resultados
print(f"La mitad de {x} es: {mitad}")
print(f"La cuarta parte de {x} es: {cuarta_parte}")
print(f"La décima parte de {x} es: {decima_parte}")
print(f"El cinco por ciento de {x} es: {cinco_por_ciento}")
print(f"El dos por ciento de {x} es: {dos_por_ciento}")